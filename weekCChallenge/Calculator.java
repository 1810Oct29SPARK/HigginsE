
public class Calculator {

	
        public double addition(int num1, int num2) {
                return num1 + num2;
        }

        public double subtraction(int num1, int num2) {
                return num1 - num2;
        }
        public double multiply(int num1, int num2) {
                return num1 * num2;
        }
        public double divide(int num1, int num2); {
                return num1 / num2;
        }

}
